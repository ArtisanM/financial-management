-- 财富水位需要两条国家级货币基准线:
--   · CPI 保命线(整体 CPI · 含食品 · 衡量购买力)
--   · M2 地位线(广义货币增速 · 衡量相对社会财富份额)
--
-- 数据策略(tech-design v0.5 决策 41):历史 seed 烤入 + 年度增量拉取。
-- 历史值是已公布的不变事实,无需反复拉 API;MacroFetchJob 每年只拉最新完整年份。
--
-- ⚠ 种子值 = 国家统计局(CPI)/ 中国人民银行(M2)历史公布值(全年/年末同比)。
--   建库种子 · 可在 /admin/integrations「宏观基准」段校正 · cron 刷新近年。
--   口径:cpi_headline = 全年 CPI 涨幅 %;m2_growth = 年末 M2 同比增速 %。
--
-- backward-compat:纯 ADD TABLE · 无 family_id(宏观数据全国统一 · 非家庭私有)· 0 风险。
-- =========================================================

CREATE TABLE macro_benchmark (
  year         INT          NOT NULL,
  cpi_headline DECIMAL(6,2) NULL,        -- 全年整体 CPI 涨幅 %(含食品)
  m2_growth    DECIMAL(6,2) NULL,        -- 年末 M2 同比增速 %
  source       VARCHAR(40)  NOT NULL DEFAULT 'seed',
  fetched_at   DATETIME(3)  NOT NULL DEFAULT CURRENT_TIMESTAMP(3)
                            ON UPDATE CURRENT_TIMESTAMP(3),
  PRIMARY KEY (year)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

